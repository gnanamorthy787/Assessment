package com.Operators.Example;

public class LogicalOperators {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		int c=30;
		
		//Logical And && Operator
		System.out.println((a<b)&&(a<c));
		//Logical OR || Operator
		System.out.println((a<b)||(a<c));
		//Logical NOT ! Operator
		System.out.println(!(a<b));

	}

}
